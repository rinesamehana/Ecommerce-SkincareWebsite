                <h1>WHAT IS BLUE LIGHT AND HOW DOES IT AFFECT SKIN?</h1>
 <p>
   
 Sunlight creates all kinds of light, including UV, visible and infrared. Half the sunlight spectrum consists of visible light. 
 The other half is made up of UV and infrared light. The blue/violet band of the visible spectrum is known as High Energy Visible Light or HEV Light. 
 Light is measured in wavelengths. Ultraviolet A (UVA) is made up of wavelengths 320 to 400 nm (nanometers) in length. Ultraviolet B (UVB) wavelengths are 280 to 320 nm. 
 And blue light is made up of wavelengths 380 to 500 nm. These wavelength measurements correlate to how deeply each can penetrate skin.   UVB rays, which have shorter wavelengths, are absorbed by the epidermis, 
 or top layer of skin.  These are the rays responsible for causing sunburn and some forms of skin cancer,
 including malignant melanoma. UVA rays are absorbed into the dermis, or middle layer of skin. UVA rays are responsible for prematurely aging your skin, causing wrinkles, 
 age spots and some cancer formation. Blue light has the longest wavelength and is known to penetrate the hypodermis, or deepest layer of skin. 
 
    
</p>
